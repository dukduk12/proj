def main():
    print(f"[send] from={SENDER_EMAIL}  to={TO_EMAIL}  count={len(EMAILS)}")

    generated_pdfs = []
    try:
        with smtplib.SMTP("smtp.gmail.com", 587) as server:
            server.starttls()
            server.login(SENDER_EMAIL, SENDER_PASSWORD)
            print("[SMTP] login ok")

            for idx, ex in enumerate(EMAILS, 1):
                print(f"[{idx:02d}/{len(EMAILS)}] sending...")

                # PDF 생성
                pdf_path = make_pdf(ex["filename"], ex["pdf_title"], ex["pdf_body"].strip())
                generated_pdfs.append(pdf_path)

                # 메일 구성
                msg = MIMEMultipart()
                msg["From"]    = SENDER_EMAIL
                msg["To"]      = TO_EMAIL
                msg["Subject"] = ex["subject"]
                msg.attach(MIMEText(ex["body"], "plain", "utf-8"))

                with open(pdf_path, "rb") as f:
                    part = MIMEApplication(f.read(), Name=pdf_path.name)
                    part["Content-Disposition"] = f'attachment; filename="{pdf_path.name}"'
                    msg.attach(part)

                server.sendmail(SENDER_EMAIL, TO_EMAIL, msg.as_string())
                print(f"         ok -> {pdf_path.name}")
                time.sleep(1.5)

        print(f"\nDone: {len(EMAILS)} emails sent.")

    except Exception as e:
        print(f"\nERROR: {e}")

    finally:
        for p in generated_pdfs:
            if p.exists():
                p.unlink()
        print("tmp PDFs cleaned.")


