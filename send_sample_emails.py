_FONT_PATH = "C:/Windows/Fonts/malgun.ttf"   # 맑은 고딕 (한글 지원)

def _clean(text: str) -> str:
    """PyMuPDF가 처리하지 못하는 특수문자를 안전한 문자로 치환"""
    return (text
            .replace("—", "-")   # em dash
            .replace("–", "-")   # en dash
            .replace("’", "'")   # curly apostrophe
            .replace("“", '"').replace("”", '"')
            .replace("─", "-")   # box drawing ─
            .replace("●", "*")   # bullet
            )

def make_pdf(filename: str, title: str, body: str) -> Path:
    doc   = fitz.open()
    font  = fitz.Font(fontfile=_FONT_PATH)

    def _new_page():
        p  = doc.new_page(width=595, height=842)
        tw = fitz.TextWriter(p.rect)
        return p, tw, 60

    page, tw, y = _new_page()

    def _flush(p, tw):
        tw.write_text(p)

    def _write(tw, page, text, size, y):
        morph = None
        tw.append((50, y), _clean(text), font=font, fontsize=size)
        return y + size + 5

    # 제목
    y = _write(tw, page, title, 14, y)
    y = _write(tw, page, "-" * 60, 8,  y)
    y += 4

    # 본문 — 40자 단위 wrap (한글은 1글자 = ~2영문 폭)
    for raw_line in body.split("\n"):
        if not raw_line.strip():
            y += 8
            if y > 810:
                _flush(page, tw)
                page, tw, y = _new_page()
            continue
        # 40글자 단위 soft wrap
        chunks, buf = [], ""
        for ch in _clean(raw_line):
            buf += ch
            if len(buf) >= 45:
                chunks.append(buf)
                buf = ""
        if buf:
            chunks.append(buf)
        for chunk in chunks:
            if y > 810:
                _flush(page, tw)
                page, tw, y = _new_page()
            y = _write(tw, page, chunk, 10, y)
        y += 2

    _flush(page, tw)
    path = Path(filename)
    doc.save(str(path))
    doc.close()
    return path


